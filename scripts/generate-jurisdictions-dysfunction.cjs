// Generate src/global/jurisdictions-dysfunction.ts from
// results/dysfunction-coding/dysfunction-codes.json. The output is a
// keyed lookup (`${jurisdiction}|${regime}|${startYear}` → dysfunction)
// consumed by build-alignment.ts.
//
// regimes.json keys ↔ jurisdictions-*.ts keys generally match, but names
// can differ slightly (e.g. "Russia/USSR" in regimes.json vs "Russia" or
// "USSR" in jurisdictions). This script reports unmatched on both sides
// so we can audit.

const fs = require("node:fs");
const path = require("node:path");

const codes = JSON.parse(fs.readFileSync(
  path.resolve(__dirname, "../results/dysfunction-coding/dysfunction-codes.json"), "utf8"));

// Read all jurisdictions-*.ts files and extract every entry's
// (jurisdiction, regime, startYear) so we can match against codes.
const jurisdictionFiles = [
  "src/global/jurisdictions-americas.ts",
  "src/global/jurisdictions-asia.ts",
  "src/global/jurisdictions-europe1.ts",
  "src/global/jurisdictions-europe2.ts",
  "src/global/jurisdictions-mena.ts",
];

const entryRe = /\{\s*jurisdiction:\s*"([^"]+)",\s*regime:\s*"([^"]+)",\s*startYear:\s*(\d+),/g;
const jurEntries = [];
for (const f of jurisdictionFiles) {
  const text = fs.readFileSync(path.resolve(__dirname, "..", f), "utf8");
  let m;
  while ((m = entryRe.exec(text)) !== null) {
    jurEntries.push({ file: path.basename(f), jurisdiction: m[1], regime: m[2], startYear: parseInt(m[3]) });
  }
}

console.log(`jurisdictions-*.ts: ${jurEntries.length} entries`);
console.log(`dysfunction-codes.json: ${codes.length} entries`);

// Match by (jurisdiction == country) && (regime == regime_name) && (startYear == start).
// Allow tolerance: strip whitespace.
const codeMap = new Map();
for (const c of codes) {
  codeMap.set(`${c.country}|${c.regime_name}|${c.start}`, c.dysfunction);
}

const matches = [];
const unmatchedJur = [];
for (const j of jurEntries) {
  const key = `${j.jurisdiction}|${j.regime}|${j.startYear}`;
  if (codeMap.has(key)) {
    matches.push({ key, dysfunction: codeMap.get(key) });
  } else {
    unmatchedJur.push(j);
  }
}

const matchedKeys = new Set(matches.map(m => m.key));
const unmatchedCodes = codes.filter(c => !matchedKeys.has(`${c.country}|${c.regime_name}|${c.start}`));

console.log(`\nMatched: ${matches.length} / ${jurEntries.length} jurisdictions entries`);
console.log(`Unmatched jurisdictions entries (${unmatchedJur.length}):`);
unmatchedJur.slice(0, 30).forEach(j => console.log(`  ${j.file}: ${j.jurisdiction} | ${j.regime} | ${j.startYear}`));
if (unmatchedJur.length > 30) console.log(`  ... +${unmatchedJur.length - 30} more`);

console.log(`\nUnmatched codes entries (${unmatchedCodes.length}):`);
unmatchedCodes.slice(0, 30).forEach(c => console.log(`  ${c.country} | ${c.regime_name} | ${c.start}`));
if (unmatchedCodes.length > 30) console.log(`  ... +${unmatchedCodes.length - 30} more`);

// Write the lookup module.
const out = `// AUTO-GENERATED by scripts/generate-jurisdictions-dysfunction.cjs.
// Source: results/dysfunction-coding/dysfunction-codes.json.
// Keyed by \`\${jurisdiction}|\${regime}|\${startYear}\`. Values 1-5 (see
// rubric in results/dysfunction-coding/). Used by build-alignment.ts.

export const JURISDICTION_DYSFUNCTION: Record<string, number> = {
${matches.map(m => `  ${JSON.stringify(m.key)}: ${m.dysfunction},`).join("\n")}
};

export function dysfunctionFactor(d: number | null | undefined): number {
  if (d == null || d <= 1) return 1.00;
  if (d <= 2) return 0.92;
  if (d <= 3) return 0.80;
  if (d <= 4) return 0.60;
  return 0.35;
}
`;
fs.writeFileSync(path.resolve(__dirname, "../src/global/jurisdictions-dysfunction.ts"), out, "utf8");
console.log(`\nWrote src/global/jurisdictions-dysfunction.ts (${matches.length} mapped entries).`);
